.MODEL SMALL
.STACK 64
PRINT MACRO ARG
    LEA DX, ARG
    MOV AX,0900H
    INT 21H
ENDM

DATA SEGMENT
    MSG1 DB 0AH,0DH,"ENTER 1ST NUMBER:$"
    MSG2 DB 0AH,0DH,"ENTER 2ND NUMBER:$"
    MSG3 DB 0AH,0DH,"RESULT:$"    
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
    START:MOV AX,DATA
    MOV DS,AX
    PRINT MSG1
    CALL READ
    MOV CL,BL
    PRINT MSG2
    CALL READ
    PRINT MSG3
    MOV AL,CL
    MUL BL
    CALL DISPLAY
    MOV AH,4CH
    INT 21H
    READ PROC NEAR
    READ1:MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV BL,10
    MUL BL
    MOV BL,AL
    MOV AH,01H
    INT 21H
    SUB AL,30H
    ADD BL,AL
    RET
    READ ENDP
DISPLAY PROC NEAR
    DISP:MOV AH,00
    MOV BL,10
    DIV BL
    MOV DL,AH
    MOV DH,00
    MOV AH,00
    ADD DX,0030H
    PUSH DX
    MOV BL,10
    DIV BL
    MOV DL,AH
    MOV DH,00
    ADD DX,0030H
    MOV AX,0200H
    INT 21H
    POP DX
    MOV AX,0200H
    INT 21H
    RET
    DISPLAY ENDP
END START
CODE ENDS
	